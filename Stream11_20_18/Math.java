package Stream11_20_18;

public class Math {
	
	static int Num2 = 19;
	static int Num1 = 34;

	public static void main(String[] args) {
		
		
		int A = Num1 + Num2;
		int M = Num1 * Num2;
		int S = Num1 - Num2;
		int D = Num1 / Num2;
		
		System.out.println(A); //Add
		System.out.println(M);//Multiply
		System.out.println(S);//Subtract
		System.out.println(D); //Devision (No Remainder)
		
		
	}

}
